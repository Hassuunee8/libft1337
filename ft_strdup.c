#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*ptr;
	int		i;

	ptr = (char *) ft_calloc (ft_strlen(src) + 1, 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
